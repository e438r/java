package com.example.dto;

public class Merchant_customize {
    private String merchant_code;
    private String tpp_code;
    private String character_code;
    private String product_code;
    private String scene;
    private String create_time;
    private String update_time;

    public String getMerchant_code() {
        return merchant_code;
    }

    public void setMerchant_code(String merchant_code) {
        this.merchant_code = merchant_code;
    }

    public String getTpp_code() {
        return tpp_code;
    }

    public void setTpp_code(String tpp_code) {
        this.tpp_code = tpp_code;
    }

    public String getCharacter_code() {
        return character_code;
    }

    public void setCharacter_code(String character_code) {
        this.character_code = character_code;
    }

    public String getProduct_code() {
        return product_code;
    }

    public void setProduct_code(String product_code) {
        this.product_code = product_code;
    }

    public String getScene() {
        return scene;
    }

    public void setScene(String scene) {
        this.scene = scene;
    }

    public String getCreate_time() {
        return create_time;
    }

    public void setCreate_time(String create_time) {
        this.create_time = create_time;
    }

    public String getUpdate_time() {
        return update_time;
    }

    public void setUpdate_time(String update_time) {
        this.update_time = update_time;
    }
}
